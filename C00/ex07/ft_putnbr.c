#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	j_num;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 14);
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
		ft_putnbr(nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		j_num = nb + 48;
		write(1, &j_num, 1);
	}
}
